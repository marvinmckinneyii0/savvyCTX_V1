'use client';

import { useState } from 'react';
import { AppTopbar } from '@/components/app-topbar';
import { SeverityBadge } from '@/components/severity-badge';
import { REPORTS, formatDateTime, formatBytes, type Report } from '@/lib/mock-data';
import { FileText, Download, RefreshCw, Eye, ChevronDown, Filter, File } from 'lucide-react';
import { useToast } from '@/hooks/use-toast';

function dqaColor(score: number) {
  if (score >= 90) return 'text-[#27AE60]';
  if (score >= 75) return 'text-[#F1C40F]';
  return 'text-[#E67E22]';
}

